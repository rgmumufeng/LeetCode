class Solution(object):
    def searchInsert(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: int
        """
        i, j = 0, len(nums)
        while i < j:
            mid = (i+j)/2
            if nums[mid] >= target:
                j = mid
            else:
                i = mid+1
        return i
    

